function renderMd(md: string): React.ReactNode {
  // Lightweight markdown renderer (h2, h3, bold, lists, links, tables, paragraphs)
  const lines = md.split("\n");
  const out: React.ReactNode[] = [];
  let i = 0;
  let key = 0;
  while (i < lines.length) {
    const line = lines[i];
    if (!line.trim()) { i++; continue; }

    if (line.startsWith("## ")) { out.push(<h2 key={key++} className="font-display text-[28px] font-medium tracking-[-0.018em] mt-12 mb-4 gradient-text-static">{line.slice(3)}</h2>); i++; continue; }
    if (line.startsWith("### ")) { out.push(<h3 key={key++} className="font-display text-[20px] font-medium text-ink mt-8 mb-3">{line.slice(4)}</h3>); i++; continue; }

    // Table
    if (line.startsWith("|") && lines[i + 1]?.startsWith("|---")) {
      const headers = line.split("|").map((s) => s.trim()).filter(Boolean);
      i += 2;
      const rows: string[][] = [];
      while (i < lines.length && lines[i].startsWith("|")) {
        rows.push(lines[i].split("|").map((s) => s.trim()).filter(Boolean));
        i++;
      }
      out.push(
        <div key={key++} className="my-6 overflow-x-auto">
          <table className="w-full text-[14.5px] glass rounded-xl overflow-hidden">
            <thead className="bg-white/[0.04]">
              <tr>{headers.map((h, k) => <th key={k} className="text-left p-3 px-4 font-medium text-ink border-b border-line">{h}</th>)}</tr>
            </thead>
            <tbody>
              {rows.map((r, ri) => (
                <tr key={ri} className="border-b border-line last:border-0">
                  {r.map((c, ci) => <td key={ci} className="p-3 px-4 text-ink-dim align-top" dangerouslySetInnerHTML={{ __html: inlineMd(c) }} />)}
                </tr>
              ))}
            </tbody>
          </table>
        </div>
      );
      continue;
    }

    // List
    if (line.startsWith("- ") || /^\d+\.\s/.test(line)) {
      const ordered = /^\d+\.\s/.test(line);
      const items: string[] = [];
      while (i < lines.length && (lines[i].startsWith("- ") || /^\d+\.\s/.test(lines[i]))) {
        items.push(lines[i].replace(/^(-\s|^\d+\.\s)/, ""));
        i++;
      }
      const ListTag = ordered ? "ol" : "ul";
      out.push(
        <ListTag key={key++} className={`my-4 space-y-2 text-[15px] text-ink-dim leading-[1.65] ${ordered ? "list-decimal" : ""} pl-6`}>
          {items.map((it, k) => <li key={k} dangerouslySetInnerHTML={{ __html: inlineMd(it) }} />)}
        </ListTag>
      );
      continue;
    }

    // Paragraph
    out.push(<p key={key++} className="my-4 text-[15px] text-ink-dim leading-[1.65]" dangerouslySetInnerHTML={{ __html: inlineMd(line) }} />);
    i++;
  }
  return out;
}

function inlineMd(s: string): string {
  return s
    .replace(/\*\*(.+?)\*\*/g, "<strong class='text-ink'>$1</strong>")
    .replace(/`(.+?)`/g, "<code class='font-mono text-[13px] bg-white/[0.05] px-1.5 py-0.5 rounded'>$1</code>")
    .replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a class="text-accent-2 hover:underline" href="$2">$1</a>');
}

